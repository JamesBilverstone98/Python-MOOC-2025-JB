# PRINT NUMBERS = print out all the even numbers between 2-30 using a loop. print each number on a new line

number = 2 # number starts at 2 since the range is between 2-30

while number <= 30: # whilst number is less than or equal to 30
    print(number) # print out starting number
    number += 2 # number counter adds 2 each time since it is only even numbers
  

    
